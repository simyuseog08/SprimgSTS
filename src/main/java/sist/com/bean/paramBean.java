package sist.com.bean;

public class paramBean {
	private String pass;
	private String job;
	
	
	public paramBean() {
		super();
	}


	public paramBean(String pass, String job) {
		super();
		this.pass = pass;
		this.job = job;
	}


	public String getPass() {
		return pass;
	}


	public void setPass(String pass) {
		this.pass = pass;
	}


	public String getJob() {
		return job;
	}


	public void setJob(String job) {
		this.job = job;
	}
	
	
	
	

}
