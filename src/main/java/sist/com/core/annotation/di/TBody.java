package sist.com.core.annotation.di;

public class TBody {
	private String tr;
	private String td;
	private String th;
	
	
	
	public TBody() {
		super();
	}



	public TBody(String tr, String td, String th) {
		super();
		this.tr = tr;
		this.td = td;
		this.th = th;
	}



	public String getTr() {
		return tr;
	}



	public void setTr(String tr) {
		this.tr = tr;
	}



	public String getTd() {
		return td;
	}



	public void setTd(String td) {
		this.td = td;
	}



	public String getTh() {
		return th;
	}



	public void setTh(String th) {
		this.th = th;
	}
	
	
	
	

}
